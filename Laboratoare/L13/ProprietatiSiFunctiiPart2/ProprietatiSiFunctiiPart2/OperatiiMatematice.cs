using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ProprietatiSiFunctiiPart2
{
    public class OperatiiMatematice
    {
        public int Adunare(int a, int b, int c)
        {
            return a + b + c;
        }

        public int Adunare(int a, int b)
        {
            return a + b;
        }

        public double Adunare(double a, double b)
        {
            return a + b;
        }

        public double Adunare(double a, double b, double c)
        {
            return a + b + c;
        }

    }
}
